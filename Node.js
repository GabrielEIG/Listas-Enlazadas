// Clase que representa un nodo en una lista enlazada
export class Nodee {
    
    data; // Dato almacenado en el nodo
    next; // Referencia al siguiente nodo en la lista

    // Constructor por defecto donde se inicializan los valores
    constructor(){
        this.data = 0; // Inicialmente el dato es 0
        this.next = null; // Inicialmente no hay siguiente nodo
    }

}
